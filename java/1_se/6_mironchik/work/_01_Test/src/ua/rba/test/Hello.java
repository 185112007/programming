package ua.rba.test;

public class Hello{	
    public static void main(String[] args){
        System.out.println("Hello world!");
    }
}			 

/* 
   1. public - heryerden ulasilabilir
   2. static - sýnýfýn nesnesini olusturmadan bu methodu cagýrabiliris
   3. void - method hicbirsey geriye dondurmuyor
   4. main - bu method java spesifikasyonunda belirlenmistir, kucuk harflerle yazilir, uygulamanýn baslangic noktasidir
   5. String - java.lang.String paketinde yerlesiyor, ve importa ihtiyac yoktur
   6. System - java.lang.System paketinde yerlesiyor, dolayýsýyla importa ihtiyac yoktur
   7. out - System sýnýfýnýn public static out adýnda bir ozelligi(property) vardýr
   8. println - out ozelligin(property) public static println adýnda metodu vardýr.
*/