package com.gafurhayytbayev;

public class KarakterTipi {

	public static void main(String[] args) {
		char ch1, ch2;
		
		ch1 = 65;
		ch2 = 'F';
		
		System.out.println("1.Atama");
		System.out.println("ch1: " + ch1);
		System.out.println("ch2: " + ch2);
		
		ch1 = (char) (ch1 + 1);
		ch2 = 'K';
		System.out.println("2.Atama");
		System.out.println("ch1: " + ch1);
		System.out.println("ch2: " + ch2);
	}

}
